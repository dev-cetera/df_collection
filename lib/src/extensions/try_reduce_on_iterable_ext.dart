extension TryReduceOnIterableExt<T> on Iterable<T> {
  /// Tries to reduce the iterable, returning null if it fails.
  T? tryReduce(T Function(T, T) combine) {
    try {
      return reduce(combine);
    } catch (_) {
      return null;
    }
  }
}
